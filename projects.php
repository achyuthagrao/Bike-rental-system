  <h1>PROJECTS</h1>
  <p><br><font size="5">The Next-Generation Pharmacist Research Program reveals what pharmacists are thinking today and what visions they hold for the future. </font></br>
    <br>When Pharmacy Times joined forces with Parata Systems to profile today’s pharmacist by conducting a national research project, the goals were lofty. What does the future of pharmacy look like in the coming years and beyond? What are the important issues that cross all sectors of pharmacy? What trends and conditions impact pharmacists in their daily professional and personal lives? Our national research project developed into a survey, conducted last spring, which consisted of personal interviews of pharmacists across the country. The results were extremely revealing—and underscored the value and unique role pharmacists play in today’s health care environment.</br>
    <p><br><font size="5">************</font></br>
    <br>The Next-Generation Pharmacist survey consisted of telephone interviews to gain in-depth feedback from individual pharmacists who work in community, national chain, and institutional settings. The findings indicate that pharmacists are seriously thinking about their profession in terms of potential changes, how they approach their work, and what they would like to do better. Historically, the profession of pharmacy has grown rapidly as new ways to deliver patient care and medication management have evolved.</br>
    <br>The role of the pharmacist has been transformed from that of a dispenser of medications to a vital health care team member, who consults closely with patients with chronic diseases, offers medication adherence strategies, and broadens the profession’s influence overall. The just-released National Association of Chain Drug Stores’ Industry Profile 2010- 2011 summarizes these changes: “The pharmacist’s role has grown and evolved over the years to become more inclusive of patients’ health care needs as a whole. Patients can now look to their pharmacy as a total health care provider, and today’s pharmacists play an important role in improving patient outcomes.”</br>
    <br>As part of the Next-Generation Pharmacist ProgramTM, we also launched a firstof- its-kind awards program that focuses on specific aspects of the profession and shines a light on the traits and characteristics of the people who chose pharmacy as their life’s work. The purpose of this awards progam is to recognize and honor those individuals who have made a difference in the profession.</br> 
